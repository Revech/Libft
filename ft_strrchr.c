#include "libft.h"

char	*ft_strrchr(const char *str, int x)
{
	int		i;
	char	c;

	i = ft_strlen((char *)str);
	c = (char)x;
	while (i >= 0)
	{
		if (str[i] == c)
			return ((char *)&str[i]);
		i--;
	}
	return (NULL);
}
